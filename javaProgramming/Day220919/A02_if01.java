package Day220919;

public class A02_if01 {
    public static void main(String[] args) {
        char c1='a';
        char c2='A';

        if(c1>c2)
        {   //조건 결과 참
            System.out.println("\'a\'>\'A\'': "+(c1>c2));
        }
        else
        {
            System.out.println("\'a\'<\'A\'': "+(c1>c2));
        }
    }
    
}
